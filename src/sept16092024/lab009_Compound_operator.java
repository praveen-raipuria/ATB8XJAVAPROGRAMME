package sept16092024;

public class lab009_Compound_operator {
    public static void main(String[] args) {
        //  Compound Assignment Operators
        int a = 10;
        // += ,-= , *=, /=
       a += 20; // a = a+ 20
       a -= 20; // a = a - 20
       a /= 10; // a = a/10
//        a*=10; // a = a*10

        System.out.println(a);
    }
}
