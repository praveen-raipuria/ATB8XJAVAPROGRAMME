package sept16092024;

public class lab003_Arithmetic_operator {
    public static void main(String[] args) {
        // Arithmetic Operator
        // +,-,/,*
        int a = 10;
        int b = 2;
        System.out.println(a+b); // + -> binary - 2 operands
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        // % - Modulus - Remainders
        System.out.println(a%b);

//        2 | 10 | 5 - Quotient
//          | 10 |
//          -------
//             0 - Remainder

        System.out.println(67%13);

//        13 | 67 | 5
//           | 65 |
//           -------
//              2

    }
}
